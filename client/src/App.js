        {/* –ü—É—Å—Ç–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ - –∫–æ–≥–¥–∞ —Å–µ—Ä–≤–µ—Ä–æ–≤ –Ω–µ—Ç */}
