            {/* –ú–∞–ø–ø–∏–º –º–∞—Å—Å–∏–≤ —Å–µ—Ä–≤–µ—Ä–æ–≤ –≤ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—ã ServerCard */}
